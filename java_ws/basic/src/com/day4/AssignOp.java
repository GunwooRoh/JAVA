package com.day4;

public class AssignOp {

	public static void main(String[] args) {
		int a = 7;
		int b = 3;
		System.out.println("a : " + a + ", b : " + b);
		a += b; //a=a+b
		System.out.println("a += b"); 
		System.out.println("a : " + a);
		a /= b; //a=a/b
		System.out.println("a /= b"); 
		System.out.println("a : " + a); 
		a*=2; //a=a*2 
	}

}
